void main ()
{

    var City = ['Dhaka','Chittagong','Sylhet','Rongpur','Rajshahi','Barishal'];

    var result1 = City.first;
    print("First Element of list = $result1");

    var result2 = City.last;
    print("Last Element of list = $result2");

    var result3 = City.length;
    print("Length Element of list = $result3");

    var result4 = City.reversed;
    print("Reversed Element of list = $result4");

    var result5 = City.isEmpty;
    print("Is Empty Element of list = $result5");

    var result6 = City.isNotEmpty;
    print("Is Not Empty Element of list = $result6");

}